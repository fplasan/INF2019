
#include <stdio.h> 
/* La función sizeof() devuelve el tamaño en bytes
   de un tipo de datos */
int main() 
{ 
    printf("char: %d bytes\n", sizeof(char)); 
    printf("int: %d bytes\n", sizeof(int)); 
    printf("float: %d bytes\n", sizeof(float)); 
    printf("double: %d bytes\n", sizeof(double)); 
    return 0; 
} 

